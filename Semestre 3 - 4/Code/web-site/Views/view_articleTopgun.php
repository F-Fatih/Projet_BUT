            <h2>Un synopsis solide</h2>
            <br>
            <p align="justify" class="indent">Le film explique qu’un groupe de voyous a bâti une usine d'enrichissement d'uranium dans une vallée profonde entourée de montagnes. Les États-Unis doivent la détruire avant que l'uranium n'arrive. Maverick, depuis longtemps relégué à un emploi de testeur d'avions expérimentaux, est appelé pour concevoir le plan d'attaque et entraîner la nouvelle équipe de recrues de Top Gun qui la conduira. </p>
            <br> 
            <p align="justify" class="indent">Le spectateur est renvoyé au courage sans faille des pilotes, à leur adhésion totale à leur mission périlleuse, dans des élans très américains déroutants. Face à la mort, aucun des soldats ne doute, pire, ils cherchent à tout prix à participer à la mission, leur seul idéal.</p>
