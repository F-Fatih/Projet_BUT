            <h2>Un film moderne</h2>
            <br>
            <p align="justify" class="indent">Cependant, Top Gun : Maverick nuance son propos, et se pose parfois en témoin critique de son ère. Ainsi, le film trouve un peu de substance au moment de mettre en avant l’utilisation de drone, sujet de polémique vif aux USA: le long métrage évacue trop rapidement le problème, mais ne manque pas de souligner que les pilotes de Top Gun sont une espèce en voie d’extinction, vouée à être remplacée par des systèmes automatisés. Implicitement, une plus grande ouverture s’affiche également à l’écran: en 1986, il n’y avait aucune femme pilote dans l’œuvre de Tony Scott, en accord avec une représentation cohérente de la réalité, désormais la gente féminine est présente, signe d’une évolution bienvenue de la société.</p>
            <br>
            <p align="justify" class="indent">Bien que "Top Gun : Maverick" soit un film d'action bourré d'adrénaline, il a également un côté émotionnel. Le personnage de Maverick est confronté à des défis personnels qui ont un impact sur sa vie professionnelle. Le film explore des thèmes tels que la famille, le deuil et la rédemption, ajoutant une dimension émotionnelle à l'histoire.</p>
