            <h2>Un retour dans le temps</h2>
            <br>
            <p align="justify" class="indent">Ainsi, dans la chronologie fictive de George R.R. Martin, le premier épisode de House of the Dragons se déroule 172 ans avant les événements de Game of Thrones. La dynastie Targaryen règne sur Westeros en tant que roi Viserys (Paddy Considine). Si les Targaryen croisent la route de personnages d'autres maisons bien connues, comme les Stark et les Lannister, House of The Dragon traite avant tout d'un drame familial. Les personnages principaux sont le roi Viserys, son frère Daemon, la fille du roi, Rhaenyra, et sa meilleure amie, Alicent. Cette dernière devient la femme de Viserys (et donc la reine), et à partir là la situation va commencer à dégénérer.</p>
            <br> 

            <h2>Un bon casting</h2>
            <br>
            <p align="justify" class="indent">Paddy Considine a assumé le rôle du roi Viserys Targaryen, un dirigeant choisi par les seigneurs de Westeros pour régner sur les sept royaumes. Ses motivations sont bonnes, mais nous savons par Game of Thrones qu'elles ne suffisent pas à faire de lui un bon leader. Matt Smith incarne le prince Daemon Targaryen, frère et héritier du roi, guerrier et cavalier de dragon. Emma Darcy est la princesse de l'écran Raenira Targaryen, la fille aînée de Viserys Targaryen et une chevaucheuse de dragons. Olivia Cooke joue Aricent Hightower, fille d'Otto Hightower, Main du Roi. Ayant grandi dans le donjon rouge, elle connaît les rouages de la politique et de la manipulation.</p>
