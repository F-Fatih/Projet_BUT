            <h2>Un retour en fanfare</h2>
            <br>
            <p align="justify" class="indent">On l'attendait tous, la série sur Mercredi Addams est enfin là ! Réalisée et produite par Tim Burton, cette nouvelle adaptation télévisée de La Famille Addams vient de faire son entrée sur le catalogue Netflix. Publiée pour la première fois dans le magazine The New Yorker dans les années 1930, la famille Addams et son mode de vie décalé n'ont jamais vieilli. Morticia, Gomez, leurs deux enfants et leur entourage unique continuent de faire rire et frissonner les fans du genre. Mais pour cette nouvelle adaptation télévisée, les studios MGM veulent que le scénario occupe le devant de la scène mercredi.</p>
            <br>

            <h2>La jeunesse au coeur de la série</h2>
            <br>
            <p align="justify" class="indent">La série dépeint des jeunes plus sadiques et imaginatifs que jamais. Et c'est l'actrice Jenna Ortega, 19 ans, qui prête son personnage à l'héroïne de Tim Burton. On l'a vue dans Yes Day, Jane the Virgin, ou encore la saison 2 de You. Elle répond à Catherine Zeta-Jones, qui interprète Morticia Addams, Luis Guzman , Gwendoline Christie, qui apparaît dans Game of Thrones, et Thora Birch.</p>
            <br> 
            <p align="justify" class="indent">Le premier épisode de la série commence avec Mercredi Addams qui intègre une nouvelle école. Elle doit naviguer dans un monde rempli d'élèves typiques, mais aussi faire face à des défis uniques en raison de sa famille excentrique. Avec son humour sombre et son intelligence acérée, Mercredi est un personnage fascinant à suivre, et on s'attache rapidement à elle.</p>
            <br>

            <h2>Une réalisation de qualité</h2>
            <br>
            <p align="justify" class="indent">Après Beetlejuice, Les Noces Funèbres et Dark Shadows, cette série est faite pour Tim Burton. Arbres tordus, ambiances sinistres, silhouettes sombres aux yeux cerclés de noir... l'univers du réalisateur s'inscrit parfaitement dans l'univers de La Famille Addams. Outre la comédie inhérente au charme clanique, le réalisateur sait donner à tous ses films une touche gothique romantique. </p>
            <br>

            <h2>Une série inclusive</h2>
            <br>
            <p align="justify" class="indent">La série "Mercredi" n'est pas seulement destinée aux fans de la famille Addams. Bien que les fans des films et des dessins animés originaux trouveront beaucoup de références et d'easter eggs à apprécier, la série est accessible à tous. Les intrigues sont bien construites et chaque épisode est rempli de rebondissements. Les nouveaux personnages introduits dans la série sont également intéressants, offrant une vision plus large de l'univers Addams.</p>
            <br>
            <p align="justify" class="indent">L'un des éléments les plus frappants de la série "Mercredi" est la manière dont elle s'attaque aux questions de discrimination et de préjugés. La série montre de manière subtile comment Mercredi et sa famille sont constamment confrontées à la discrimination en raison de leur différence. Les messages de tolérance et de compréhension sont très importants dans notre société actuelle, et la série est un excellent moyen d'engager des conversations importantes sur ces sujets.</p>
